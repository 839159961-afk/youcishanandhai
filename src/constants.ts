export type SceneType = 'COURTYARD' | 'WORLD_MAP' | 'ITEMS' | 'ALMANAC' | 'MARKET';

/** 與主界面插畫一致的天候（決定粒子與左上角「天候」標題） */
export type WeatherType = 'sunny' | 'rainy' | 'autumn' | 'snowy' | 'dusk';

/** `public/` 下靜態資源 URL（帶上 Vite `base`，避免子路徑部署時 404） */
export function publicAssetUrl(filePath: string): string {
  const base = import.meta.env.BASE_URL;
  const rel = filePath.replace(/^\//, '');
  return `${base}${rel}`;
}

