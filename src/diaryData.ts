export function parseImportedDiaryEntries(raw: string): ImportedDiaryEntry[] {
  const result: ImportedDiaryEntry[] = [];
  const lines = raw
    .replace(/\u200f/g, '')
    .replace(/\r\n?/g, '\n')
    .split('\n');

  const headerPattern = /^第\s*(\d+)\s*篇(?:\s*[：:]\s*|\s+)(.+)$/;

  let currentLevel: number | null = null;
  let currentTitle = '';
  let bodyLines: string[] = [];

  const flush = () => {
    if (currentLevel == null) return;
    result.push({
      level: currentLevel,
      title: currentTitle.trim(),
      body: bodyLines.join('\n').trim(),
    });
  };

  for (const rawLine of lines) {
    const line = rawLine.trimEnd();
    const m = line.trim().match(headerPattern);
    if (m) {
      flush();
      currentLevel = Number(m[1]);
      currentTitle = m[2] ?? '';
      bodyLines = [];
      continue;
    }
    if (currentLevel != null) bodyLines.push(line);
  }

  flush();

  result.sort((a, b) => a.level - b.level);
  return result;
}

